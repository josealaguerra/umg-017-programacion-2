    private void llenaListado() {
        List<venta> listaVentas = null;
        
        try{
            vDAO = new ventaDAO();
            listaVentas = new ArrayList<>();
            listaVentas = vDAO.seleccionaTodo();

            Object[]pObj=new Object[5];

            modelo = (DefaultTableModel)TablaDatosVenta.getModel();

            for(venta p:listaVentas){
                pObj[0] = p.getId_venta();
                pObj[1] = p.getId_cliente();
                pObj[2] = p.getFecha_venta();
                pObj[3] = p.getNumero_factura();
                pObj[4] = p.getMonto_total();                
                modelo.addRow(pObj);
            }
            TablaDatosVenta.setModel(modelo);
        }catch(Exception e){
            Util.printException("fventa_1.llenaListado", e);
        }
    }
    
    private void llenaListadoDetalle(int paramIDVenta) {
        List<venta_detalle> listaVentaDetalle = null;
        
        try{
            listaVentaDetalle = new ArrayList<>();
            listaVentaDetalle = cdDAO.seleccionaTodo( paramIDVenta );

            Object[]pObj=new Object[7];

            modelo = (DefaultTableModel)TablaDatosVentaDetalle.getModel();

            for(venta_detalle p:listaVentaDetalle){
                pObj[0] = p.getId_venta_detalle();
                pObj[1] = p.getId_venta();
                pObj[2] = p.getId_marca_producto();
                pObj[3] = p.getId_tipo_producto();
                pObj[4] = p.getId_producto();
                pObj[5] = p.getPrecio_unitario_venta();
                pObj[6] = p.getCantidad();
                modelo.addRow(pObj);
            }
            TablaDatosVentaDetalle.setModel(modelo);
        }catch(Exception e){
            Util.printException("fventa_detalle.llenaListado", e);
        }
    }

    
    
    private void agregar(){
        
        try{
            miVenta = new venta(Util.str2int( this.txtid_venta.getText() ),
                                Util.str2int( this.txtid_cliente.getText() ),
                                this.txtfecha_venta.getDate() ,
                                this.txtnumero_factura.getText(),
                                Util.str2double( this.txtmonto_total.getText() ) );

            if ( vDAO.inserta(miVenta) ){
                JOptionPane.showMessageDialog(null, "Venta ingresado");
                limpiaTabla();
                limpiaCampos();
                llenaListado();
            }
                    
            
        }catch(Exception e){
            Util.printException("fventa_1.agregar", e);
        }
    }
    
