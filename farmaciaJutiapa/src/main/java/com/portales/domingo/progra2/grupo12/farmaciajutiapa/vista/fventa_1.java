    public void limpiaTabla(){
        int filaRestante=0;
        try{
            if(TablaDatosVenta.getRowCount() > 0){
                for(int i=0;i<=TablaDatosVenta.getRowCount();i++){
                    modelo.removeRow(i);
                    i = i - 1;
                    filaRestante=modelo.getRowCount();
                    if(filaRestante==0){
                        break;
                    }
                }
            }
        }catch(Exception e){
            Util.printException("fventa_1.limpiaTabla", e);
        }        
    }
    
